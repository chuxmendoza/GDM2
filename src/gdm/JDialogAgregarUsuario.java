    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
      

        try{
            btnAceptar.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            if(!editar){
            if(!txtLogin.getText().trim().isEmpty() &&!txtPass.getText().trim().isEmpty()&&
                !txtPass2.getText().trim().isEmpty()&&!txtUsuario.getText().trim().isEmpty()&&comboBoxPerfil.getSelectedIndex()>0){

                if(!UsuarioNegocio.ValidarLogin(txtLogin.getText())){

                    if(!txtPass.getText().equals(txtPass2.getText())){
                        JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("DiferentesPass")
                            , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloUsuario"), JOptionPane.INFORMATION_MESSAGE);
                        return;
                    }
                    if(UsuarioNegocio.Guardar(txtLogin.getText(), txtPass.getText(), txtUsuario.getText(), Perfil.getEnum(comboBoxPerfil.getSelectedIndex()))){
                        JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("UsuarioAgregado")
                            , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloUsuario"), JOptionPane.INFORMATION_MESSAGE); 
                        this.DialogResult = true;
                        this.dispose();
                        

                    }else{
                        JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("ErrorMensaje")
                            , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloError"), JOptionPane.INFORMATION_MESSAGE);

                    }
                }else{
                    JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("UsuarioRepetido")
                        , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloUsuario"), JOptionPane.INFORMATION_MESSAGE);
                }

            }else{
                JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("CamposVacios")
                    , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloUsuario"), JOptionPane.INFORMATION_MESSAGE);
            }
            
            }else{
                
            
            if(!txtLogin.getText().trim().isEmpty() &&!txtPass.getText().trim().isEmpty()&&
                !txtPass2.getText().trim().isEmpty()&&!txtUsuario.getText().trim().isEmpty()&&comboBoxPerfil.getSelectedIndex()>0){

               

                    if(!txtPass.getText().equals(txtPass2.getText())){
                        JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("DiferentesPass")
                            , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloUsuario"), JOptionPane.INFORMATION_MESSAGE);
                        return;
                    }
                    if(UsuarioNegocio.Editar(id,txtLogin.getText(), txtPass.getText(), txtUsuario.getText(), Perfil.getEnum(comboBoxPerfil.getSelectedIndex()))){
                        JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("UsuarioEditado")
                            , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloUsuario"), JOptionPane.INFORMATION_MESSAGE); 
                        this.DialogResult = true;
                        this.dispose();
                    }else{
                        JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("ErrorMensaje")
                            , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloError"), JOptionPane.INFORMATION_MESSAGE);

                    }
               

            }else{
                JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("CamposVacios")
                    , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloUsuario"), JOptionPane.INFORMATION_MESSAGE);
            }
            
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("ErrorMensaje")
                , ResourceBundle.getBundle("gdm/entidades/clases/resource").getString("TituloError"), JOptionPane.INFORMATION_MESSAGE);

        }finally{
             btnAceptar.setCursor(Cursor.getDefaultCursor());
        }

    }//GEN-LAST:event_btnAceptarActionPerformed

