    private void cargarEdicion() {
        try{
                   
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        Usuario usuario = UsuarioNegocio.Obtener(id);
        if(usuario != null){
            txtLogin.setText(usuario.getLogin());
            txtUsuario.setText(usuario.getNombre());
            comboBoxPerfil.setSelectedIndex(usuario.getPerfil().getValor());     
            txtPass.setText(usuario.getPass());
            txtPass2.setText(usuario.getPass());
          
        }
     }catch(Exception e){
           
     }
        finally{
          this.setCursor(Cursor.getDefaultCursor());     
        }
  
   }
